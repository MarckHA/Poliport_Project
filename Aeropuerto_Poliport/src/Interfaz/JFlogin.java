    private void JTFusuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTFusuarioKeyPressed
        JTFusuario.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS, java.util.Collections.EMPTY_SET);
        String texto=this.JTFusuario.getText();
        if(evt.getKeyCode()==KeyEvent.VK_ENTER || evt.getKeyCode()==KeyEvent.VK_TAB){
            if(texto.equals("")){
                JOptionPane.showMessageDialog(null, "Por favor ingrese el usuario",
                        "Información",JOptionPane.WARNING_MESSAGE);
            }
            else{
                this.JPFpassword.requestFocus();
            }
        }
    }//GEN-LAST:event_JTFusuarioKeyPressed

    private void JPFpasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JPFpasswordKeyPressed
        JPFpassword.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS, java.util.Collections.EMPTY_SET);
        if(evt.getKeyCode()==KeyEvent.VK_ENTER || evt.getKeyCode()==KeyEvent.VK_TAB){
            if(Arrays.equals(this.JPFpassword.getPassword(), new char[]{' '})){
                JOptionPane.showMessageDialog(null, "Por favor ingrese la clave",
                        "Información",JOptionPane.WARNING_MESSAGE);
            }
            else{
                this.JBaceptar.requestFocus();
            }
        }
    }//GEN-LAST:event_JPFpasswordKeyPressed

