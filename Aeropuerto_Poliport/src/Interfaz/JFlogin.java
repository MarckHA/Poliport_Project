    private boolean lecturaRegex(String comprobación, String patron){
        Pattern pat= Pattern.compile(patron);
        Matcher mat= pat.matcher(comprobación);
        return mat.matches();
    }
    
    public void ejecucion(){
        jfcliente = new JFCliente(identificador);
        switch(identificador){
            case 1:
                String password = cambioCharaString(this.JPFpassword.getPassword());
                Connection con = conexion.getConexion(this.JTFusuario.getText(), password);
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Conexión establecida con éxito");
                break;
            case 2:
                String patron="^(\\d{10})$";
                if(lecturaCliente()){
                    cedula = JTFciPasaporte.getText();
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Bienvenido de nuevo");//hacer animación en cuadro de dialogo.
                } else{
                    if(lecturaRegex(this.JTFciPasaporte.getText(), patron)){
                        JOptionPane.showMessageDialog(null, "Cliente no encontrado.\nPorfavor ingrese sus datos.","Información",
                             JOptionPane.INFORMATION_MESSAGE);
                        jfcliente.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(null, "El número ingresado debe contener 10 dígitos.","Información",
                             JOptionPane.INFORMATION_MESSAGE);
                    } 
                }
                break;
            default: 
                break;
        }

    }
    
