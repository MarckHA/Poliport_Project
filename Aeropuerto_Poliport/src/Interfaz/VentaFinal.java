    private void JBcomprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBcomprarActionPerformed
        perAsientos = new PersistenciaAsientosBD();
        perBoleto = new PersistenciaBoletoBD();
        String idAsientos="";
        for(Asiento aux: listaAsientos.getAsientosEscogidos()){
            idAsientos+= aux.getNumeroAsiento()+"\n";
        }        
        int opcion = JOptionPane.showConfirmDialog(null, "Está seguro que desea realizar la siguiente compra: \n"+
                "Cédula: "+JLcedula.getText()+"\nID del vuelo: "+JLidVuelo.getText()+"\nID del avión: "+JLidAvion.getText()+
                        "\nID Asientos: "+idAsientos+"\nPrecio: "+JLtotal.getText());
        if(opcion == JOptionPane.YES_OPTION){
            for(Asiento aux: listaAsientos.getAsientosEscogidos()){
                perAsientos.actualizarAsiento(aux.getNumeroAsiento(), JLidAvion.getText(), "Ocupado");
                idBoleto++;
                perBoleto.registrarBoleto(String.valueOf(idBoleto), JLcedula.getText(), JLidVuelo.getText(), aux.getNumeroAsiento());
                JOptionPane.showMessageDialog(null, "Su compra fue realizada con éxito");
                menu = new JFMenu();
                menu.idBoleto=idBoleto;
                this.setVisible(false);
                menu.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Transacción cancelada");
        }
 
        
        
    }//GEN-LAST:event_JBcomprarActionPerformed

