    public void sacarInformacionCliente(String cedula){
        String condicion = cedula;
        cliente.mostrarClientes(JTdatos, 2, condicion);
        JTdatos.setVisible(false);
        DefaultTableModel modelo = (DefaultTableModel) JTdatos.getModel();
        JLcedula.setText((String) modelo.getValueAt(0, 0));
        JLnombres.setText((String) modelo.getValueAt(0, 1));
        JLfechaNac.setText((String) modelo.getValueAt(0, 2));
        JLsexo.setText((String) modelo.getValueAt(0, 3));
    }
    
    public void sacarInformacionVuelo(String idVuelo) {
        String condicion = idVuelo;
        vuelo.mostrarVuelos(JTdatos, 2, condicion);
        DefaultTableModel modelo = (DefaultTableModel) JTdatos.getModel();
        JLidVuelo.setText((String) modelo.getValueAt(0, 0));
        JLidAvion.setText((String) modelo.getValueAt(0, 1));
        JLfechaSal.setText((String) modelo.getValueAt(0, 2));
        JLorigen.setText((String) modelo.getValueAt(0, 3));
        JLdestino.setText((String) modelo.getValueAt(0, 4));
        JLhora.setText((String) modelo.getValueAt(0, 5));
        sacarInformacionAvion();
    }
    
    public void sacarInformacionAvion(){
        String condicion = JLidAvion.getText();
        avion.mostrarAviones(JTdatos, 2, condicion);
        DefaultTableModel modelo = (DefaultTableModel) JTdatos.getModel();
        JLaerolinea.setText((String) modelo.getValueAt(0, 1));
        JLmodelo.setText((String) modelo.getValueAt(0, 2));
    }
    
    public void informacionBoletos(){
        String salida = "\t"+numBoletos;
        JLnumBoletos.setText(salida);
        if(boletosDescuento==0){
            jCBNo.setSelected(true);
            jCBYes.setSelected(false);
            JLbolDescuentos.setText(boletosDescuento+"");
        } else {
            jCBNo.setSelected(false);
            jCBYes.setSelected(true);
            JLbolDescuentos.setText(boletosDescuento+"");
        }
        
    }

