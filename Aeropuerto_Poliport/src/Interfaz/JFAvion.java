    private void sacarDatos() {
        int itemAerolinea=0;
        int itemModelo=0;
        DefaultTableModel modeloTabla = (DefaultTableModel) jTAvionConsultar.getModel();
        idAvion = (String) modeloTabla.getValueAt(0, 0);
        for( int i=1; i<this.JCBaerolineaActualizar.getItemCount();i++){
            if(lecturaRegex(this.JCBaerolineaActualizar.getItemAt(i), (String) modeloTabla.getValueAt(0, 1))){
                itemAerolinea=i;
                break;
            }
        }  
        this.JCBaerolineaActualizar.setSelectedIndex(itemAerolinea);
        for( int i=1; i<this.JCBmodeloActualizar.getItemCount();i++){
            if(lecturaRegex(this.JCBmodeloActualizar.getItemAt(i), (String) modeloTabla.getValueAt(0, 2))){
                itemModelo=i;
                break;
            }
        }  
        this.JCBmodeloActualizar.setSelectedIndex(itemModelo);
    }
    
