    private void JBactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBactualizarActionPerformed
        ArrayList<String> atributosActualizar = new ArrayList<String>();
        if (jCBAvionIDActualizar.isSelected()){atributosActualizar.add("avionID= '" + jTFAvionIDActualizar.getText()+"'"); }
        if (jCBAerolineaActualizar.isSelected()){atributosActualizar.add("aerolinea= '" + JCBaerolineaActualizar.getSelectedItem() +"'"); }
        if (jCBModeloActualizar.isSelected()){atributosActualizar.add("modelo= '" + JCBmodeloActualizar.getSelectedItem()+"'"); }
        String atributoActualizar = "avionID";
        String condicion = "'" +jTFAvionIDActualizar.getText()+"'" ;
        per.actualizarAvion(atributosActualizar, atributoActualizar, condicion);
        mostrarTabla();
        this.jTFAvionIDActualizar.setEnabled(false);
        this.JCBaerolineaActualizar.setEnabled(false);
        this.JCBmodeloActualizar.setEnabled(false);
    }//GEN-LAST:event_JBactualizarActionPerformed

