    private boolean lecturaIDAvion(String comprobacion){
        boolean flag1=false;
        String patron="^(\\d{3})$";
        if(lecturaRegex(comprobacion, patron)){
            flag1=true;
        } else {
            this.jTFAvionIDActualizar.setText("");
            this.JCBaerolineaActualizar.setSelectedIndex(0);
            this.JCBmodeloActualizar.setSelectedIndex(0);
            editables();
        }
        return flag1;
    }
    
    private boolean lecturaRegex(String comprobación, String patron){
        Pattern pat= Pattern.compile(patron);
        Matcher mat= pat.matcher(comprobación);
        return mat.matches();
    }
    
