    private void jTFAvionIDConsultarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFAvionIDConsultarKeyReleased
        ArrayList<String> atributosMostrar = new ArrayList<>();
        ArrayList<String> nombresColumnas = new ArrayList<>();
        atributosMostrar.add("avionID"); nombresColumnas.add("Avion ID");
        if (jCBAerolineaConsultar.isSelected()) {atributosMostrar.add("aerolinea");nombresColumnas.add("Aerolinea");}
        if (jCBModeloConsultar.isSelected()) {atributosMostrar.add("modelo"); nombresColumnas.add("Modelo");}
        String atributoBuscar = "avionID";
        String condicion = "'%" + jTFAvionIDConsultar.getText() + "%'";
        per.consultarAvion(jTAvionConsultar, atributosMostrar, atributoBuscar, condicion, nombresColumnas);
    }//GEN-LAST:event_jTFAvionIDConsultarKeyReleased

