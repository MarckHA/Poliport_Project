    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        String codigoCedula = this.jTFAvionIDEliminar.getText();
         if(per.verificacionAvion(codigoCedula)){
            per.mostrarAviones(jTAvionConsultar, 2, codigoCedula);
            sacarDatos();
            int respuesta = JOptionPane.showConfirmDialog(null,"¿Está seguro que desea eliminar el siguiente registro?\n"+
                        "ID del avión: "+idAvion+"\nAerolínea: "+JCBaerolineaActualizar.getSelectedItem()+"\nModelo: "+JCBmodeloActualizar.getSelectedItem()
                    , "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(respuesta== JOptionPane.YES_OPTION){
                per.eliminarAvion(codigoCedula);
                per.mostrarAviones(jTEliminarAvion,1,"");
            } 
        } else {
            JOptionPane.showMessageDialog(null, "El número de avión ingresado no existe."
                    + "\nPor favor revise su entrada.","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

