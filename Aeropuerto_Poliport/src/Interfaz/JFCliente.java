    private void JTFcedulaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTFcedulaKeyPressed
        JTFcedula.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS, java.util.Collections.EMPTY_SET);
        if(evt.getKeyCode()==KeyEvent.VK_ENTER || evt.getKeyCode()==KeyEvent.VK_TAB){
            if(!lecturaCI(JTFcedula.getText())){
                JOptionPane.showMessageDialog(null, "Ingrese una cedula valida. \n(10 digitos)");
                this.JTFcedula.setText("");
            } else{
                this.jDCfechaNacim.requestFocus();
            }         
        }
    }//GEN-LAST:event_JTFcedulaKeyPressed
    
    
