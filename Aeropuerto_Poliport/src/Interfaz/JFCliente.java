    /**
     * Método para sacar los datos de un cliente en base a su número de cédula.
     */
    private void sacarDatos() {
        int itemSexo=0;
        DefaultTableModel modelo = (DefaultTableModel) JTconsultar.getModel();
        cedula = (String) modelo.getValueAt(0, 0);
        nombre = (String) modelo.getValueAt(0, 1);
        fechaNac = (String) modelo.getValueAt(0, 2);
        for( int i=1; i<=this.JCBsexoActualizar.getItemCount();i++){
            if(lecturaRegex(this.JCBsexoActualizar.getItemAt(i), (String) modelo.getValueAt(0, 3))){
                itemSexo=i;
                break;
            }
        }  
        this.JCBsexoActualizar.setSelectedIndex(itemSexo);
    }
