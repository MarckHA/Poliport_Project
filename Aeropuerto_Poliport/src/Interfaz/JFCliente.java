    /**
     * Método para leer que la cédula tenga 10 dígitos, este método nos ayuda a sacar los datos
     * en el Método KeyReleased de Consultar y Actualizar.
     * @param comprobacion
     * @return 
     */
    private boolean lecturaCI(String comprobacion){
        boolean flag1=false;
        String patron="^(\\d{10})$";
        if(lecturaRegex(comprobacion, patron)){
            flag1=true;
        } else {
            this.jTFCedulaActualizar.setText("");
            this.jTFNombreActualizar.setText("");
            this.JDCfechaNacActualizar.setCalendar(null);
            this.JCBsexoActualizar.setSelectedIndex(0);
            editables();
        }
        return flag1;
    }
    /**
     * Método para leer que comparar strings mediante expresiones regulares.
     * @param comprobación
     * @param patron
     * @return 
     */
    private boolean lecturaRegex(String comprobación, String patron){
        Pattern pat= Pattern.compile(patron);
        Matcher mat= pat.matcher(comprobación);
        return mat.matches();
    }
